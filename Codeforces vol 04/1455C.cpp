#include<bits/stdc++.h>
#define ll long long int
#define show(x) cout << #x << " : " << x << endl

using namespace std;

int main()
{
      ll tc;
      scanf("%I64d",&tc);

      while(tc--){

            ll x,y;
            cin >> x >> y;
            cout << x-1 << " " << y << endl;
      }

      return 0;
}
